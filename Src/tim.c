void PWM_Init(void) //PWM启动（有关闭函数）
{
	HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
}

void PWM(uint8_t ch,float Hz,uint8_t Duty)//PWM通道、频率、占空比
{
uint16_t	Period =(uint16_t)60000/Hz;//频率最大到65k
uint16_t	Compare = Period*Duty/100; //占空比0-100%
	switch(ch)
  {
		case 1:
		__HAL_TIM_SET_AUTORELOAD(&htim1, Period);	
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3,Compare); break;     
		case 2:
		__HAL_TIM_SET_AUTORELOAD(&htim3, Period);		
		__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1,Compare); break;  	
    default:return;
  }
}

void RPM(uint32_t tacho)//转速
{
	float RPMrate=109;//转速的减速比在这里修改
	float Hz=tacho/RPMrate;//获取频率
  PWM(1,Hz,50);	//转速取一通道，占空比50%
}

void SPD(uint32_t speed)//车速
{
	float SPDrate=0.8;//车速的减速比在这里修改
	float Hz=speed/SPDrate;//获取频率
  PWM(2,Hz,50);	//转速取二通道，占空比50%
}
